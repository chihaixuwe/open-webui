import { WEBUI_BASE_URL } from '$lib/constants';

export const getRuntimeState = async (token: string) => {
	let error = null;

	const res = await fetch(`${WEBUI_BASE_URL}/api/ai/runtime/state`, {
		method: 'GET',
		headers: {
			Accept: 'application/json',
			'Content-Type': 'application/json',
			authorization: `Bearer ${token}`
		}
	})
		.then(async (res) => {
			if (!res.ok) throw await res.json();
			return res.json();
		})
		.catch((err) => {
			error = err;
			console.error(err);
			return null;
		});

	if (error) {
		throw error;
	}

	return res;
};

