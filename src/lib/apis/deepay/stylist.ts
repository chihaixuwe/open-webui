import { WEBUI_BASE_URL } from '$lib/constants';

export const stylistChat = async (token: string, message: string) => {
	let error = null;

	const res = await fetch(`${WEBUI_BASE_URL}/api/ai/stylist/chat`, {
		method: 'POST',
		headers: {
			Accept: 'application/json',
			'Content-Type': 'application/json',
			authorization: `Bearer ${token}`
		},
		body: JSON.stringify({ message })
	})
		.then(async (res) => {
			if (!res.ok) throw await res.json();
			return res.json();
		})
		.catch((err) => {
			error = err;
			console.error(err);
			return null;
		});

	if (error) {
		throw error;
	}

	return res;
};

